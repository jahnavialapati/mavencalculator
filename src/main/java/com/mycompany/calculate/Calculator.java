package com.mycompany.calculate;

/**
 * calculator
 *
 */
public class Calculator
{
	public int addNumbers(int valueOne, int valueTwo) {
		return valueOne+valueTwo;
	}
	
	public int subtractNumbers(int valueOne, int valueTwo) {
		return valueOne-valueTwo;
	}
	
	public int multiplicationNumbers(int valueOne, int valueTwo) {
		return valueOne*valueTwo;
	}
	
	public double divisionNumbers(int valueOne, int valueTwo) {
		return valueOne/valueTwo;
	}
}




